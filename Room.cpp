Room::Room(RoomType type, int x, int y, Grid* grid)
{
vector<vector<int>> spawnGrid = vector<vector<int>>(45, vector<int>(25, -1));
vector<vector<int>> special1Grid = vector<vector<int>>(45, vector<int>(25, -3));
vector<vector<int>> special2Grid = {{0}};
vector<vector<int>> special3Grid = {{0}};
vector<vector<int>> exitGrid = vector<vector<int>>(45, vector<int>(25, -4));
  this->type = type;
  this->grid = grid;
  this->x = x;
  this->y = y;
  this->cornerX = x * this->grid->cols / 4;
  this->cornerY = y * this->grid->rows / 4;
  this->centerX = this->cornerX + (this->grid->cols / 8);
  this->centerY = this->cornerY + (this->grid->rows / 8);
  this->visited = 0;
  this->design = vector<vector<int>>(45, vector<int>(25, 0));
  this->enemies = {};
  this->entities = {};

  int random;

  switch(type)
  {
    case SPAWN:
    {
      this->design = spawnGrid;
      
      break;
    }
    case COMBAT:
      random = rand() % 4;
      switch(random)
      {
        case 0:
        this->design = combat1;
        break;
        case 1:
        this->design = combat2;
        break;
        case 2:
        this->design = combat3;
        break;
        case 3:
        this->design = combat4;
        break;
      }
      break;
    case SPECIAL:
      this->design = special1Grid;
      break;
    case EXIT:
      this->design = exitGrid;
      break;
  }
}

void Room::start()
{
  if(this->visited) return;
    //this->grid->map[this->centerX][this->centerY] = 1;
  int spawnX;
  int spawnY;
  switch(this->type)
  {
    case SPAWN:
      
      
      break;
    case COMBAT:
      {
        int slimes = rand() % 4 + 3;
        int monkeys = rand() % 3 + 2;
        for(int i = 0; i < slimes; i++)
        {
          do
          {
          spawnX = this->cornerX + (rand() % (this->grid->cols / 4));
          spawnY = this->cornerY + (rand() % (this->grid->rows / 4));
          }while(this->grid->map[spawnX][spawnY] > 0);
          this->createEnemy(SLIME, spawnX, spawnY);
        }
        for(int i = 0; i < monkeys; i++)
        {
          do
          {
          spawnX = this->cornerX + (rand() % (this->grid->cols / 4));
          spawnY = this->cornerY + (rand() % (this->grid->rows / 4));
          }while(this->grid->map[spawnX][spawnY] > 0);
          this->createEnemy(MONKEY, spawnX, spawnY);
        }
        break;
      }
    case SPECIAL:
      
      break;
    case EXIT:
      spawnX = this->cornerX + (rand() % (this->grid->cols / 4));
      spawnY = this->cornerY + (rand() % (this->grid->rows / 4));
      this->grid->map[spawnX][spawnY] = -5;
      break;
  }
  this->visited = 1;
}


void Room::update(float playerX, float playerY, float time)
{
  switch(this->type)
  {
    case SPAWN:
      
      
      break;
    case COMBAT:
      for(int i = 0; i < this->enemies.size(); i++)
      {
        if(!this->enemies[i]->isDead()) this->enemies[i]->goTo(playerX, playerY, time);
      }
      break;
    case SPECIAL:
      
      break;
    case EXIT:
      {
        if(this->grid->identifyMap(this->grid->player->x, this->grid->player->y) == -5)
          this->grid->player->levelUp = 1;
        break;
      }
  }
}

void Room::killing()
{
  for(int i = 0; i < this->enemies.size(); i++) 
  {
    if(this->enemies[i]->isDead())
    {
      delete this->enemies[i];
      this->enemies.erase(this->enemies.begin() + i);
    }
  }
}

void Room::drawTo(RenderWindow &window)
{
  if(this->type == COMBAT)
  {
    for(int i = 0; i < this->enemies.size(); i++)
      this->enemies[i]->drawTo(window);
  }
}

void Room::createEnemy(EnemyType type, int spawnX, int spawnY)
{
  int health;
  int speed;
  int sprite;
  switch(type)
  {
    case SLIME:
      health = 5; 
      speed = 5;
      sprite = 1;
      break;
    case MONKEY:
      health = 9;
      speed = 3;
      sprite = 2;
      break;
  }
  this->enemies.push_back(new Enemy(type, health, speed, this->grid->textures[sprite], this->grid->playing, this->grid, spawnX, spawnY));
}